    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        GuestManager guestManager = HotelMain.getGuestManager();
        try {
            Long.parseLong(phoneTextField.getText());
            Integer.parseInt(dateYearTextField.getText());
            Integer.parseInt(dateMonthTextField.getText());
            Integer.parseInt(dateDayTextField.getText());
        } catch (NumberFormatException ex) {
            return;
        }
        Guest guest = new Guest(null, nameTextField.getText(), addressTextField.getText(), Long.parseLong(phoneTextField.getText()),
                new Date(Integer.parseInt(dateYearTextField.getText()), Integer.parseInt(dateMonthTextField.getText()), Integer.parseInt(dateDayTextField.getText())));
        guestManager.createGuest(guest);
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

